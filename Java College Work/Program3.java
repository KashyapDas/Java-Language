// Swap 2 number 
public class Program3 {
    public static void main(String[] args) {
        int a = 10, b= 20;
        System.out.println("Value of a is : "+a+" and value of b is : "+b);
        int temp = a;
        a = b;
        b = temp;
        System.out.println("Value of a is : "+a+" and value of b is : "+b);
    }
}
