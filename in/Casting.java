
public class Casting {
    public static void main(String[] args){
        // Casting in Java - Implicit and Explicit

        int finalMarks = 100;
        // Explicit(Manual) Casting in Java
        int includingGST = finalMarks + (int)18.0;
        System.out.println(includingGST);

        // Implicit Casting in Java
        int age = 16;
        double finalAge = age + 2.00;
        System.out.println(finalAge);

    }   
}
