
public class LoopInJava {
    public static void main(String[] args)
    {
        String[] name = {"kashyap","jyoti","das"};

        // for loop usage
        for(int i=0; i<name.length; i++)
        {
            System.out.println(name[i]);
        }

        // while loop usage
        int i = 0;
        while(i<10)
        {
            System.out.println("Hey");
            i++;
        }
        

    }
    
}
